let currentLang = "en";

function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Swap price display for Ukrainian
  const priceAmount = document.getElementById("priceAmount");
  if (priceAmount) {
    priceAmount.textContent = lang === "uk" ? "200 \u0433\u0440\u043d" : "$5";
  }

  const flag = document.getElementById("langFlag");
  flag.textContent = lang === "en" ? "UA" : "EN";

  localStorage.setItem("coinstack-lang", lang);
}

document.getElementById("langToggle").addEventListener("click", () => {
  setLanguage(currentLang === "en" ? "uk" : "en");
});

// Restore saved language preference
const saved = localStorage.getItem("coinstack-lang");
if (saved && saved !== currentLang) {
  setLanguage(saved);
}
